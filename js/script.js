function sideMenuController(){
	var x = document.getElementById('side-bar')
	x.style.display = 'block';
	var y = document.getElementById('dark-underlay')
	y.style.display = 'block'
}
function closeMenu(){
	var x = document.getElementById('side-bar')
	x.style.display = 'none'
	var y =  document.getElementById('dark-underlay')
	y.style.display = 'none'
}